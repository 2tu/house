package com.tu.house.model.response;

/**
 * @author Tu enum@foxmail.com.
 */
public class StatsExcel {

  private int bottomline;

  private int col;
  private int colspan;
  private String data;
  private int leftline;
  private int rightline;
  private int row;
  private int rowspan;
  private String sort;
  private int topline;

  public int getBottomline() {
    return bottomline;
  }

  public void setBottomline(int bottomline) {
    this.bottomline = bottomline;
  }

  public int getCol() {
    return col;
  }

  public void setCol(int col) {
    this.col = col;
  }

  public int getColspan() {
    return colspan;
  }

  public void setColspan(int colspan) {
    this.colspan = colspan;
  }

  public String getData() {
    return data;
  }

  public void setData(String data) {
    this.data = data;
  }

  public int getLeftline() {
    return leftline;
  }

  public void setLeftline(int leftline) {
    this.leftline = leftline;
  }

  public int getRightline() {
    return rightline;
  }

  public void setRightline(int rightline) {
    this.rightline = rightline;
  }

  public int getRow() {
    return row;
  }

  public void setRow(int row) {
    this.row = row;
  }

  public int getRowspan() {
    return rowspan;
  }

  public void setRowspan(int rowspan) {
    this.rowspan = rowspan;
  }

  public String getSort() {
    return sort;
  }

  public void setSort(String sort) {
    this.sort = sort;
  }

  public int getTopline() {
    return topline;
  }

  public void setTopline(int topline) {
    this.topline = topline;
  }
}
